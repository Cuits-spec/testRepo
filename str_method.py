# 将字符串'adcd'转成大写
str1 = 'adcd'
print(str1.upper())

# 计算字符串'cd'在字符串'adcd'中出现的位置
print(str1.find('cd'))

# 用，号分割字符串'a,b,c,d'
str2 = 'a,b,c,d'
print(str2.split(','))

# str3 = 'Python is good' 将Python替换成python
str3 = 'Python is good'
print(str3.lower())

